struct ProcessInfo;

int
sys_fork(void)
{
  return fork();
}

int
sys_exit(void)
{
  exit();
  return 0;  // not reached
}

int
sys_wait(void)
{
  return wait();
}

int
sys_kill(void)
{
  int pid;

  if(argint(0, &pid) < 0)
    return -1;
  return kill(pid);
}

int
sys_getpid(void)
{
  return myproc()->pid;
}

int
sys_sbrk(void)
{
  int addr;
  int n;

  if(argint(0, &n) < 0)
    return -1;
  addr = myproc()->sz;
  if(growproc(n) < 0)
    return -1;
  return addr;
}

int
sys_sleep(void)
{
  int n;
  uint ticks0;

  if(argint(0, &n) < 0)
    return -1;
  acquire(&tickslock);
  ticks0 = ticks;
  while(ticks - ticks0 < n){
    if(myproc()->killed){
      release(&tickslock);
      return -1;
    }
    sleep(&ticks, &tickslock);
  }
  release(&tickslock);
  return 0;
}

// return how many clock tick interrupts have occurred
// since start.
int
sys_uptime(void)
{
  uint xticks;

  acquire(&tickslock);
  xticks = ticks;
  release(&tickslock);
  return xticks;
}

// added for ps
int
sys_getprocs (void)
{
  // init a ProcessInfo struct to be passed into getprocs and populated
  struct ProcessInfo *pinfo;

  // "if the syscall cannot be completed for any reason, it shall return -1"
  // argptr defined in syscall.c: checks if int of a user ptr is in the user part of the addr space
  // argptr returns -1 if err, or 0 otherwise
  if (argptr(0,(char**)&pinfo,sizeof(struct ProcessInfo)*NPROC)<0) {
    return -1;
  }

  // else call getprocs to populate our table
  return getprocs(pinfo);

}
